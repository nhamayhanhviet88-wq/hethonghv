    // Load pending calls + sources
    try {
        var [cr, sr] = await Promise.all([apiCall('/api/import/fabric-pending-calls'), apiCall('/api/import/sources')]);
        _bnhFab.groups = cr.groups || [];
        _bnhFab.availSources = sr.sources || [];
    } catch (e) { showToast(e.message || 'Lỗi tải dữ liệu', 'error'); return; }

    // Code will be generated by backend
    _bnhFab.code = '(Tự động tạo khi lưu)';

    var now = new Date();
    var dd = String(now.getDate()).padStart(2, '0'), mm = String(now.getMonth() + 1).padStart(2, '0'), yyyy = now.getFullYear();
    _bnhFab.dateStr = dd + '/' + mm + '/' + yyyy;

    var user = (typeof currentUser !== 'undefined' && currentUser) ? currentUser : {};
    _bnhFab.userName = user.full_name || user.username || '—';
    _bnhFab.userDept = user.department_name || '';

    // Build modal
    var ov = document.createElement('div');
    ov.id = '_fabOv';
